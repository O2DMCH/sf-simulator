    public decimal GetGoldValueForItem(int characterLevel)
    {
        decimal goldValue;

        if (characterLevel >= GoldValues.Count)
        {
            goldValue = 2500000;
        }
        else
        {
            goldValue = GoldValues[characterLevel];
        }

        // 15% for item to have half the gold value
        if (_random.NextDouble() <= 0.15)
            return goldValue * 0.5M;

        return goldValue;
    }
